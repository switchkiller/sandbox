int powerfunc(int x, int n, std::vector<int>& vals){
  int s = 0;
  for (auto val : vals){
    s += pow(val,n);
  }

  if (s == x)
    return 1;

  int answer = 0;
  int v = vals.empty() ? 1 : vals.back()+1;
  while (s+pow(v,n) <= x){
    vals.push_back(v);
    answer += powerfunc(x,n,vals);
    vals.pop_back();
    v++;
  }
  return answer;
}

