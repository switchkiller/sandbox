int solve(int l, int r, int mid, int k){
  if (l < r){
    int pos = l + (r-l)/2;
    if (pos == k) return mid;
    if (pos > k) return solve(l, pos-1, mid-1, k);
    return solve(pos+1, r, mid-1, k);
  }
  return 0;
}

